export function getModelBySlug(slug: string | undefined): Model | undefined {
    if (!slug) return undefined;
    const decoded = decodeURIComponent(slug).toLowerCase();
    return models.find(m =>
        m.slug.toLowerCase() === decoded ||
        m.slug.normalize("NFD").replace(/[\u0300-\u036f]/g, "").toLowerCase() === decoded
    );
}
export function getModelsByBrand(brandId: string): Model[] {
    return models.filter(m => m.brandId === brandId);
}
export function getCategoryBySlug(slug: string | undefined): Category | undefined {
    if (!slug) return undefined;
    const decoded = decodeURIComponent(slug).toLowerCase();
    return categories.find(c => c.slug.toLowerCase() === decoded);
}
