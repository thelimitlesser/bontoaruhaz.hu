export function getProducts(filters: { brandId?: string; modelId?: string; categoryId?: string; subcategoryId?: string; partItemId?: string }): Product[] {
    return products.filter(p => {
        if (filters.brandId && p.brandId !== filters.brandId) return false;
        if (filters.modelId && p.modelId !== filters.modelId) return false;
        if (filters.categoryId && p.categoryId !== filters.categoryId) return false;
        if (filters.subcategoryId && p.subcategoryId !== filters.subcategoryId) return false;
        if (filters.partItemId && p.partItemId !== filters.partItemId) return false;
        return true;
    });
}
export function getPartItemsBySubcategory(subcategoryId: string): PartItem[] {
    return partItems.filter((p: PartItem) => p.subcategoryId === subcategoryId);
}

export function getAllUniqueSubcategoriesForSearch(): { value: string; label: string }[] {
    const unique = new Map<string, string>();
    partsSubcategories.forEach((s: Subcategory) => {
        if (!unique.has(s.name)) {
            unique.set(s.name, s.id);
        }
    });
    return Array.from(unique.entries()).map(([name, id]) => ({ value: id, label: name }));
}

